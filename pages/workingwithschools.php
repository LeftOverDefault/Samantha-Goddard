        <div class="content">
            <p>I specialise in supporting primary and secondary school students, from Year 5 to Year 13, who are grappling with worry, anxiety, and stress, empowering them to manage their emotions effectively.</p>
            <p>Girls often struggle with unkind behaviour in friendship groups, feeling excluded from conversations, or believing they are different or disliked. This can lead to withdrawal, increased anxiety, and behaviours like disordered eating or self-harm. Many girls feel isolated and unseen, spending breaks alone or avoiding certain classes.</p>
            <p>Boys, on the other hand, tend to express anxiety and low self-esteem through behaviour, often exhibiting anger, aggression, or rudeness. They may internalise their feelings, leading to physical symptoms like headaches or stomach aches.</p>
            <p>The students I work with often express feeling unstable in their friendships, alone, and undervalued. This negatively impacts their self-esteem, leaving them feeling disconnected and hesitant to speak up.</p>
            <p>Through practical exercises and education on how their minds function, students learn to take control of their emotional responses, understanding the power of their thoughts.</p>
            <p>This approach helps them:</p>
            <ul>
                <li>Boost their self-esteem and confidence</li>
                <li>Feel more connected and calm</li>
                <li>Take control of their thoughts and reactions</li>
                <li>Cultivate positivity and ease</li>
                <li>Develop healthy friendships and boundaries</li>
                <li>Feel empowered to seek help when needed</li>
            </ul>
            <p>My sessions align with the PSHE curriculum on mental health and emotional well-being, providing valuable support to students.</p>
